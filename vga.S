# a0 = PCI Config Entry for QEMU VGA
_setup_qemu_vga:
	push ra

	push a0
	la a0, setup_qemu_vga
	call _writeln
	pop a0

	# mode 13h
	# code from https://gist.github.com/iamgreaser/15a0a81cd117d4efd1c47ce598c13c91
	mv t0, a0

	# Set up frame buffer
	la t1, _vga_framebuffer
	sw t1, 0x10(t0)

	# Set up I/O
	la t2, _vga_mmio
	sw t2, 0x18(t0)

	# Enable memory accesses for this device
	lw a0, 0x04(t0)
	ori a0, a0, 0x02
	sw a0, 0x04(t0)
	lw a0, 0x04(t0)

	# Set up video mode somehow
	li t3, 0x60 # Enable LFB, enable 8-bit DAC
	sh t3, 0x508(t2)

	# Set Mode 13h by hand
	la a0, mode_13h_regs
	addi a1, t2, 0x400-0xC0
	la t3, 0xC0
	1:
		# Grab address
		lbu a3, 0(a0)
		beq a3, zero, 2f
		add a2, a1, a3

		# Grab index and data
		lb a4, 1(a0)
		lbu a5, 2(a0)

		# Advance a0
		addi a0, a0, 3

		# If this is for the attribute controller,
		# treat it specially.
		blt a3, t3, 3f
		# If this is an external register,
		# also treat it specially.
		blt a4, zero, 4f

			# Normal case
			sb a4, 0(a2)
			sb a5, 1(a2)
			j 1b

		3:
			# The attribute controller is a special case
			lb zero, 0xDA(a1)
			sb a4, 0(a2)
			sb a5, 0(a2)
			j 1b

		4:
			# External registers are also special
			# but not as special as the attribute controller
			sb a5, 0(a2)
			j 1b
	2:


	# Set up a palette
	li t3, 0
	sb t3, 0x408(t2)
	li t3, 0
	li t4, 256*3
	la a0, initial_palette
	1:
		lb t5, 0(a0)
		sb t5, 0x409(t2)
		addi a0, a0, 1
		addi t3, t3, 1
		bltu t3, t4, 1b


	# Draw Purple Background
	la t0, _vga_framebuffer
	li t2, 0
	_draw_loop:
	add t3, t0, t2
	li t1, 0xFFFFFFFFFFFFFFFF
	sd t1, 0x0(t3)
	li t1, 1
	add t2, t1, t2
	li t3, 0xFFFF
	bne t2, t3,_draw_loop

	pop ra
	ret

mode_13h_regs:
	# Miscellaneous Output Register:
	# Just a single port.
	# But bit 0 determines whether we use 3Dx or 3Bx.
	# So we need to set this early.
	.byte 0xC2, 0xFF, 0x63

	# Sequencer:
	# Disable reset here.
	.byte 0xC4, 0x00, 0x00

	# Attributes:
	# - Read 3DA to reset flip-flop
	# - Write 3C0 for address
	# - Write 3C0 for data
	.byte 0xC0, 0x00, 0x00
	.byte 0xC0, 0x01, 0x02
	.byte 0xC0, 0x02, 0x08
