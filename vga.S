	.byte 0xD4, 0x09, 0x41
	.byte 0xD4, 0x0A, 0x20
	.byte 0xD4, 0x0B, 0x1F
	.byte 0xD4, 0x0C, 0x00
	.byte 0xD4, 0x0D, 0x00
	.byte 0xD4, 0x0E, 0xFF
	.byte 0xD4, 0x0F, 0xFF
	.byte 0xD4, 0x10, 0x9C
	.byte 0xD4, 0x11, 0x8E # Registers are now reprotected
	.byte 0xD4, 0x12, 0x8F
	.byte 0xD4, 0x13, 0x28
	.byte 0xD4, 0x14, 0x40
	.byte 0xD4, 0x15, 0x96
	.byte 0xD4, 0x16, 0xB9
	.byte 0xD4, 0x17, 0xA3

	# Sequencer
	.byte 0xC4, 0x01, 0x01
	.byte 0xC4, 0x02, 0x0F
	.byte 0xC4, 0x03, 0x00
	.byte 0xC4, 0x04, 0x0E

	.byte 0x00

